$t=0
$x=96
$y=24
$flip=0

def TIC
	dir=0
	if btn(0) then # up
		$y-=1
	end
	if btn(1) then # down
		$y+=1
	end
	if btn(2) then # left
		$x-=1;dir=-1
	end
	if btn(3) then # right
		$x+=1;dir=1
	end

	_y=$y+40
	_x=$x+40
	vbank(0)
	cls(15)
	vbank(1)
	cls(0)
	spr_walk(256,$x,$y,dir,$t)
	print("Hello, Grass!",_x,$y+20,14)
	$t+=1
end

def spr_walk(id,x,y,dir,t)
 i=t/8%4
 i=1 if dir==0 or i==3
	id+=i*4
	if dir<0 then
		f=0
	elsif dir>0 then
		f=1
	else
		f=$flip
	end
	$flip=f
	vbank(1)
	spr(id,x,y,0,1,f,0,4,4)
end

