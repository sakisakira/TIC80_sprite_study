def TIC
	dir=0
	dx,dy=0,0
	if btn(0) then # up
		dy=-1
	end
	if btn(1) then # down
		dy=1
	end
	if btn(2) then # left
		dx=-1;dir=-1
	end
	if btn(3) then # right
		dx=1;dir=1
	end
	if btnp(4) then # A
		$condor.change_mode
	end
	len=(dx*dx+dy*dy)**0.5
	if len>0 then
		$x+=dx/len
		$y+=dy/len
	end

	vbank(0)
	cls(14)
	vbank(1)
	cls(0)
	$condor.update
	$grass.shadow($x,$y)
	$condor.shadow
	if $y<$condor.y then
		$grass.show($x,$y,0,0,$tic)
		$condor.show
	else
		$condor.show
		$grass.show($x,$y,0,0,$tic)
	end
	$grass.baloon
	$condor.baloon
	$tic+=1
end


