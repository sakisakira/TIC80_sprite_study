class Girl
	attr_reader(:last_x,:last_y,:direction)
	W_Base=256-256
	WSBase=262-256
	WNBase=320-256
	N_Base=268-256
	S_Base=332-256

	def initialize(base_id)
		@base_id=base_id
		@start_tic=0
		@last_x,@last_y=0,0
		@direction=[0,1]
		case base_id
		when GrassID
			@sdiff=[8,28]
		when CondorID
			@sdiff=[8,31]
		end
	end

	def sprite_id(d_x,d_y)
		if d_x.zero? then
			return @base_id+N_Base if d_y<0
			return @base_id+S_Base if d_y>0
			return nil
		end
		return @base_id+WNBase if d_y<0
		return @base_id+W_Base if d_y==0
		return @base_id+WSBase if d_y>0
		return nil
	end

	def show(x,y,tx,ty,tic,dist=nil)
		d_x=x-@last_x
		d_y=y-@last_y
		if d_x.zero? and d_y.zero? then
			@start_tic=tic
			d_t=1
		else
			@direction=[d_x<=>0,d_y<=>0]
		end
		d_t=(tic-@start_tic)/8%4
		d_t=1 if d_t==3
		d_t=1 if dist and dist.abs<=0.5
		flip=if @direction[0]>0 or (@direction[0]==0 and d_t==2)
			then 1 else 0 end
		if d_x.zero? and d_y.zero? then
			sid=sprite_id(@direction[0],@direction[1])+2
		elsif d_x.zero? then
			sid=sprite_id(0,d_y<=>0)+d_t%2*2
		else
			sid=sprite_id(d_x<=>0,d_y<=>0)+d_t*2
		end
		@x=x.to_i
		@y=y.to_i
		vbank(1)
		spr(sid,x.to_i+tx,y.to_i+ty,0,1,flip,0,2,4)
		@last_x,@last_y=x,y
	end

	def shadow(x,y)
		vbank(0)
		cx=x+@sdiff[0]
		cy=y+@sdiff[1]
		elli(cx,cy,6,3,15)
	end

	def baloon
		vbank(0)
		x0=@x+12
		y0=@y-12
		x1=x0+15
		y1=y0+15
		col=15
		line(x0+1,y0,x1-1,y0,col)
		line(x0+1,y1,x1-1,y1,col)
		line(x0,y0+1,x0,y1-1,col)
		line(x1,y0+1,x1,y1-1,col)
		pix(x0+1,y0+1,col)
		pix(x1-1,y0+1,col)
		pix(x0+1,y1-1,col)
		pix(x1-1,y1-1,col)
		spr(224,x0,y0,0,1,0,0,2,2)
	end

	def beard(x0,y0,x1,y1)
		bcx=(x0+x1)/2.0 # baloon center
		bcy=(y0+y1)/2.0
		gcx=@x+8.0 # girl head center
		gcy=@y+8.0
		dx=bcx-gcx
		dy=gcy=gcy
		prj=3
		if dy.abs>dx.abs then
		  if dy>0 then
			bx=inter(gcy,bcy,gcx,bcx,y1)
			edx=dx/dy*prj
			beard_h(x0,x1,y1,bx,bx-edx,y1+prj)
		  else
			bx=inter(gcy,bcy,gcx,bcx,y0)
			edx=dx/dy*prj
			beard_h(x0,x1,y0,bx,bx-edx,y1-prj)
		  end
		else
		  if dx>0 then
			by=inter(gcx,bcx,gcy,bcy,x0)
			edy=dy/dx*prj
			beard_v(x0,y0,y1,x0-prj,by-edy)
		  else
			by=inter(gcx,bcx,gcy,bcy,x1)
			edy=dy/dx*prj
			beard_v(x1,y0,y1,x1+prj,by-edy)
		  end
		end
	  end
	private :beard

	def beard_h(x0,x1,y,bx,ex,ey)
	  bx0=bx-3
	  bx1=bx+3
	  bx0=x0 if bx0<x0
	  bx1=x1 if bx1>x1
	  bcol=0
	  tri(bx0,y,bx1,y,ex,ey,bcol)
	  fcol=15
	  line(bx0,y,ex,ey,fcol)
	  line(bx1,y,ex,ey,fcol)
	end
	private :beard_h

	def beard_v(x,y0,y1,by,ex,ey)
	  by0=by-3
	  by1=by+3
	  by0=y0 if by0<y0
	  by1=y1 if by1>y1
	  bcol=0
	  tri(x,by0,x,by1,ex,ey,bcol)
	  fcol=15
	  line(x,by0,ex,ey,fcol)
	  line(x,by1,ex,ey,fcol)
	end
	private :beard_v

	def inter(s0,s1,d0,d1,s)
		d0+(d1-d0)/(s1-s0)*(s-s0)
	end
	private :interpolate
end # Girl

