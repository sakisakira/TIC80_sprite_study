class FollowerStatus
	attr_reader(:x,:y)
	module Mode
		Neutral = 0
		Happiness = 1
		Anger = 2
		Sadness = 3
	end
	ModeNames = {
		Mode::Neutral => 'Neutral',
		Mode::Happiness => 'Happiness',
		Mode::Anger => 'Anger',
		Mode::Sadness => 'Sadness'
	}
	ModeNumber=ModeNames.size

	def initialize(base_id)
		@girl= Girl.new(base_id)
		@x=rand(Width)
		@y=rand(Height)
		@x_t=0
		@y_t=0
		@tic=0
		@angle=0
		@mode=Mode::Neutral
		@target_dist=999
		AvgDist=20
		MaxSpeed=1.25
	end

	def follow(girl)
		@followee=girl
	end

	def update
		@x_t,@y_t=0,0
		case @mode
		when Mode::Neutral
			update_neutral
		when Mode::Happiness
			update_happiness
		when Mode::Anger
			update_anger
		when Mode::Sadness
			update_sadness
		else
			print("illegal mode",@mode)
		end
	end

	def orbit_dir_dist(avg_dist)
		dx=@followee.last_x-@x
		dy=@followee.last_y-@y
		f_dist=[(dx*dx+dy*dy)**0.5,0.5].max
		@followee_dist=f_dist
		dx/=f_dist
		dy/=f_dist
		dist=(f_dist-avg_dist).abs
		if f_dist>=avg_dist then
			[dx,dy,dist]
		else
			[-dx,-dy,dist]
		end
	end

	def target_dir(avg_dist,angle)
		target_x=@followee.last_x+avg_dist*Math::cos(angle)
		target_y=@followee.last_y+avg_dist*Math::sin(angle)
		dx=target_x-@x
		dy=target_y-@y
		dist=[(dx*dx+dy*dy)**0.5,0.5].max
		@target_dist=dist
		dx/=dist
		dy/=dist
		[dx,dy,dist]
	end

	def update_pos(max_speed,avg_dist,angle,ratio)
		o_dx,o_dy,o_dist=orbit_dir_dist(avg_dist)
		t_dx,t_dy,t_dist=target_dir(avg_dist,angle)
		dx=ratio*o_dx+(1-ratio)*t_dx
		dy=ratio*o_dy+(1-ratio)*t_dy
		len=[(dx*dx+dy*dy)**0.5,0.5].max
		dist=ratio*(o_dist-avg_dist)+(1-ratio)*t_dist
		speed=(dist/avg_dist)*max_speed
		speed=[[-max_speed,speed].max,max_speed].min
		dx_=(dx/len)*speed
		dy_=(dy/len)*speed
		@x+=dx_
		@y+=dy_
		@x=[[0,@x].max,Width].min
		@y=[[0,@y].max,Height].min
		@target_dist=avg_dist
		@tic+=1
	end

	def keep_distance(max_speed,avg_dist)
		dx=@followee.last_x-@x
		dy=@followee.last_y-@y
		f_dist=[(dx*dx+dy*dy)**0.5,0.5].max
		speed=((f_dist-avg_dist)*1.0/avg_dist)*max_speed
		speed=[[-max_speed,speed].max,max_speed].min
		dx_=(dx/f_dist)*speed
		dy_=(dy/f_dist)*speed
		@x+=dx_
		@y+=dy_
		@x=[[0,@x].max,Width].min
		@y=[[0,@y].max,Height].min
		@target_dist=avg_dist
		@followee_dist=f_dist
		@tic+=1
	end

	def move_around(max_speed,avg_dist,ratio)
		period=8*60 # in tics.
		@angle=(@tic%period)*2*Math::PI/period
		update_pos(max_speed,avg_dist,@angle,ratio)
	end

	def update_neutral
		keep_distance(MaxSpeed*0.5,AvgDist)
		if @target_dist<AvgDist*1.1 then
	  		move_around(MaxSpeed*0.5,AvgDist,0)
		else
			move_around(MaxSpeed*0.5,AvgDist,1)
		end
	end

	def jump(t,height,duration)
		a=-4*height/(duration**2)
		a*(t-duration/2)**2+height
	end

	def update_happiness
		keep_distance(MaxSpeed,AvgDist)
		dt=@tic%60
		height=10.0
		duration=10.0
		if dt<=duration then
			@y_t=-jump(dt,height,duration)
		else
			@y_t=0
		end
	end

	def update_anger
		keep_distance(MaxSpeed*1.5,AvgDist*2)
	end

	def update_sadness
		keep_distance(MaxSpeed*0.6,AvgDist*1.5)
	end

	def show
		vbank(1)
		print("Mode: "+ModeNames[@mode],@x,@y-12,12)
		dist_diff=@followee_dist-@target_dist
		@girl.show(@x,@y,@x_t,@y_t,@tic,dist_diff)
	end

	def change_mode(mode=nil)
		if mode then
			@mode=mode
		else
			@mode=(@mode+1)%ModeNumber
		end
	end

	def shadow
		@girl.shadow(@x,@y)
	end

	def baloon
		@girl.baloon
	end
end # FollowerStatus

