	end
end

class Runner
	attr_accessor(:position)
	attr_reader(:speed, :power)
	attr_reader(:parameter,:status)

	# (1-SpeedDiff)**300==1/2, half-life in 5sec.
	SpeedDiff=1-2**(-1.0/300.0)
	StdSpeed=1000.0/60.0 # [m/s]
	StdTicks=(1600.0/StdSpeed)*60
	ResistanceRatio=SpeedDiff/StdSpeed

	def initialize(seed)
		@parameter=GirlParameter.new(seed)
		@status=GirlStatus.new(1.0,1.0,1.0,1.0)
		@power=1.0
		@speed=0.0
		@position=[0.0,0.5]
	end

	def lung
		@parameter.lung
	end

	def muscle
		@parameter.muscle
	end

	def nerve
		@parameter.nerve
	end

	def judgement
		@parameter.judgement
	end

	def weight
		@parameter.weight
	end

	def simulate(tic,runners,distance)
		@tic=tic
		@runners=runners
		@distance=distance
		@group_indices=nil
		@closest=nil
		@predecessor=nil
		@position[0]+=@speed/60
		@speed=updated_speed
		t=remaining_tic
		@status=@status-@status/t if t>0
	end

	def updated_speed
		speed+adj_accel-adj_resist
	end

	def adj_resist
		ResistanceRatio*weight*speed
	end

	def adj_accel
		a=power_ratio/weight
		SpeedDiff*a
	end

	def power_ratio
		t=remaining_tic
		return 0.0 if t<=0
		d_s=@status/t
		w=weight
		l=lung
		m=muscle
		ratio=(d_s.s*w+d_s.o*l+d_s.f)/3
		StdTicks*ratio
	end
	
	def elapsed_sec
		@tic/60.0
	end

	def remaining_distance
		@distance-position[0]
	end

	def remaining_tic
		[0,((remaining_distance/target_speed)*60).to_i].max
	end

	def target_speed
		1000.0/60.0
	end

	def target_time
		rem_sec=remaining_distance/target_speed
		sec=elapsed_sec+rem_sec
		[sec,@distance/target_speed].min
	end

	def group_indices
		return @group_indices if @group_indices
		max_dist=2.0
		indices=[@runners.index(self)]
		begin
			new_indices=[]
			indices.each do |i|
				r_i=@runners[i]
				@runners.size.times do |j|
					next if indices.include?(j)
					r_j=@runners[j]
					diff=r_j.position[0]-r_i.position[0]
					next if diff.abs>max_dist
					new_indices<<j
				end
			end
			indices=(indices+new_indices).sort.uniq
		end until new_indices.empty?
		@group_indices=indices
	end

	def group_length
		pos_min=Float.MAX
		pos_max=-FLoat.MAX
		group_indices.each do |i|
			r=@runner[i]
			pos_min=[pos_min,r.position[0]].min
			pos_max=[pos_max,r.position[0]].max
		end
		pos_max-pos_min
	end

	def group_count
		group_indices.size
	end

	def closest
		return @closest if @closest
		diff_i=[Float::MAX,-1]
		@runners.each_with_index do |r,i|
			next if self==r
			diff=r.position[0]-self.position[0]
			if diff.abs<diff_i[0].abs then
				diff_i=[diff,i]
			end
		end
		@closest=diff_i
	end

	def predecessor
		return @predecessor if @predecessor
		diff_i=[Float::MAX,-1]
		@runners.each_with_index do |r,i|
			next if self==r
			diff=r.position[0]-self.position[0]
			if 0<diff and diff_i[0] then
				diff_i=[diff,i]
			end
		end
		@predecessor=diff_i
	end

	def distance_top
		top_pos=@runners.map {|r| r.position[0]}.max
		top_pos-self.position[0]
	end
end # Runner

