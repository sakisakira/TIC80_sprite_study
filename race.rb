class Girl
	attr_reader(:last_x,:last_y,:direction,:baloon,:mode)

	module Mode
		Neutral = 0
		Happiness = 1
		Anger = 2
		Sadness = 3
	end
	ModeNames = {
		Mode::Neutral => 'Neutral',
		Mode::Happiness => 'Happiness',
		Mode::Anger => 'Anger',
		Mode::Sadness => 'Sadness'
	}
	ModeNumber=ModeNames.size

	W_Base=256-256
	WSBase=262-256
	WNBase=320-256
	N_Base=268-256
	S_Base=332-256
	@@shown_girls = []

	def initialize(base_id)
		@base_id=base_id
		@start_tic=0
		@last_x,@last_y=0,0
		@direction=[0,1]
		case base_id
		when GrassID
			@sdiff=[8,28]
		when CondorID
			@sdiff=[8,31]
		end
		@mode=Mode::Neutral
		@baloon_bgcol=14
		@baloon_lcol=15
	end

	def change_mode(mode=nil)
		if mode then
			@mode=mode
		else
			@mode=(@mode+1)%ModeNumber
		end
	end

	def mode_sprite_id
		224+@mode*2
	end
	private :mode_sprite_id

	def sprite_id(d_x,d_y)
		if d_x.zero? then
			return @base_id+N_Base if d_y<0
			return @base_id+S_Base if d_y>0
			return nil
		end
		return @base_id+WNBase if d_y<0
		return @base_id+W_Base if d_y==0
		return @base_id+WSBase if d_y>0
		return nil
	end

	def show(x,y,tx,ty,tic,dist=nil)
		d_x=x-@last_x
		d_y=y-@last_y
		if d_x.zero? and d_y.zero? then
			@start_tic=tic
			d_t=1
		else
			@direction=[d_x<=>0,d_y<=>0]
		end
		d_t=(tic-@start_tic)/8%4
		d_t=1 if d_t==3
		d_t=1 if dist and dist.abs<=0.5
		flip=if @direction[0]>0 or (@direction[0]==0 and d_t==2)
			then 1 else 0 end
		rdx,rdy=refine_direction(d_x,d_y)
		if rdx.zero? and rdy.zero? then
			sid=sprite_id(@direction[0],@direction[1])+2
		elsif rdx.zero? then
			sid=sprite_id(0,rdy<=>0)+d_t%2*2
		else
			sid=sprite_id(rdx<=>0,rdy<=>0)+d_t*2
		end
		@x=x.to_i
		@y=y.to_i
		vbank(1)
		spr(sid,x.to_i+tx,y.to_i+ty,0,1,flip,0,2,4)
		@last_x,@last_y=x,y
		@baloon=[@x+9,@y-12] # initial baloon position
		@@shown_girls << self
	end

	def refine_direction(dx,dy)
		return [0,0] if dx.zero? and dy.zero?
		threshold=Math::tan(Math::PI/8)
		if dx.abs>dy.abs then
			if (dy/dx).abs<threshold then
				return [dx<=>0,0]
			end
		else
			if (dx/dy).abs<threshold then
				return [0,dy<=>0]
			end
		end
		[dx<=>0,dy<=>0]
	end
	private :refine_direction

	def show_shadow(x,y)
		vbank(0)
		cx=x+@sdiff[0]
		cy=y+@sdiff[1]
		elli(cx,cy,6,3,15)
	end

	def self.show_baloon
	  baloon_width=16
	  cluster=find_cluster(baloon_width)
	  if not cluster.empty? then
		center=baloon_center(cluster)
		cluster.each do |g|
		  g.keep_dist(center,baloon_width)
		end
	  end
	  @@shown_girls.each do |g|
		g.show_baloon
	  end
	  @@shown_girls = []
	end

	def self.find_cluster(dist)
		pairs=[]
		count=@@shown_girls.size
		(0...count).each do |i0|
			g0=@@shown_girls[i0]
			((i0+1)...count).each do |i1|
				g1=@@shown_girls[i1]
				if g0.baloon_dist(g1)<dist then
					pairs<<[i0,i1]
				end
			end
		end
		return [] if pairs.empty?
		cluster=pairs[0]
		pairs.each do |i0,i1|
			if cluster.include?(i0) or cluster.include?(i1) then
				cluster+=[i0,i1]
			end
		end
		cluster.sort.uniq.map do |i|
			@@shown_girls[i]
		end
	end

	def self.baloon_center(girls)
		x,y=0,0
		girls.each do |g|
			x+=g.baloon[0]
			y+=g.baloon[1]
		end
		x/=girls.size
		y/=girls.size
		[x,y]
	end

	def keep_dist(center,dist)
		if (center[0]-@baloon[0]).abs<dist then
			if center[0]<@baloon[0] then
				@baloon[0]=center[0]+dist/2
			else
				@baloon[0]=center[0]-dist/2
			end
		elsif (center[1]-@baloon[1]).abs<dist then
			if center[1]<@baloon[1] then
				@baloon[1]=center[1]+dist/2
			else
				@baloon[1]=center[1]-dist/2
			end
		end
	end
	private :keep_dist

	def baloon_dist(other)
		b0=@baloon
		b1=other.baloon
		[(b0[0]-b1[0]).abs,(b0[1]-b1[1]).abs].max
	end
	private :baloon_dist

	def show_baloon
		vbank(1)
		x0=@baloon[0]
		y0=@baloon[1]
		x1=x0+15
		y1=y0+15
		rect(x0+1,y0+1,x1-x0-1,y1-y0-1,@baloon_bgcol)
		col=@baloon_lcol
		line(x0+1,y0,x1-1,y0,col)
		line(x0+1,y1,x1-1,y1,col)
		line(x0,y0+1,x0,y1-1,col)
		line(x1,y0+1,x1,y1-1,col)
		pix(x0+1,y0+1,col)
		pix(x1-1,y0+1,col)
		pix(x0+1,y1-1,col)
		pix(x1-1,y1-1,col)
		show_spike(x0,y0,x1,y1)
		spr(mode_sprite_id,x0,y0,0,1,0,0,2,2)
	end
	private :show_baloon

	def show_spike(x0,y0,x1,y1)
		bcx=(x0+x1)/2.0 # baloon center
		bcy=(y0+y1)/2.0
		gcx=@x+8.0 # girl head center
		gcy=@y+8.0
		dx=bcx-gcx
		dy=gcy=gcy
		prj=3
		if dy.abs>dx.abs then
		  if dy>0 then
			bx=inter(gcy,bcy,gcx,bcx,y1)
			edx=dx/dy*prj
			show_spike_h(x0,x1,y1,bx,bx-edx,y1+prj)
		  else
			bx=inter(gcy,bcy,gcx,bcx,y0)
			edx=dx/dy*prj
			show_spike_h(x0,x1,y0,bx,bx-edx,y1-prj)
		  end
		else
		  if dx>0 then
			by=inter(gcx,bcx,gcy,bcy,x0)
			edy=dy/dx*prj
			show_spike_v(x0,y0,y1,by,x0-prj,by-edy)
		  else
			by=inter(gcx,bcx,gcy,bcy,x1)
			edy=dy/dx*prj
			show_spike_v(x1,y0,y1,by,x1+prj,by-edy)
		  end
		end
	  end
	private :show_spike

	def show_spike_h(x0,x1,y,sx,ex,ey)
		w=6
		sy0,sy1=y,y
		sx0=sx-w/3
		sx1=sx+w/3
		if sx0<x0 then
			sx0=x0
			sy0=y-(ey-y<=>0)*(w-(sx1-x0))
		end
		if sx1>x1 then
			sx1=x1
			sy1=y-(ey-y<=>0)*(w-(x1-sx0))
		end
		bcol=@baloon_bgcol
		tri(sx0,sy0,sx1,sy1,ex,ey,bcol)
		fcol=@baloon_lcol
		line(sx0,sy0,ex,ey,fcol)
		line(sx1,sy1,ex,ey,fcol)
	end
	private :show_spike_h

	def show_spike_v(x,y0,y1,sy,ex,ey)
		w=6
		sx0,sx1=x,x
		sy0=sy-3
		sy1=sy+3
		if sy0<y0 then
			sy0=y0
			sx0=x-(ex-x<=>0)*(w-(sy1-y0))
		end
		if sy1>y1 then
			sy1=y1
			sx1=x-(ex-x<=>0)*(w-(y1-sy0))
		end
		bcol=@baloon_bgcol
		tri(sx0,sy0,sx1,sy1,ex,ey,bcol)
		fcol=@baloon_lcol
		line(sx0,sy0,ex,ey,fcol)
		line(sx1,sy1,ex,ey,fcol)
	end
	private :show_spike_v

	def inter(s0,s1,d0,d1,s)
		d0+(d1-d0)/(s1-s0)*(s-s0)
	end
	private :inter
end # Girl

